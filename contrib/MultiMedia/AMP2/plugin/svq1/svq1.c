  yuv[0] = malloc(width * height);
  yuv[1] = malloc(width * height / 4);
  yuv[2] = malloc(width * height / 4);

  video_w = width;
  video_h = height;
  video_gray = amp->gray;

  amp->total_frames = 0;
  amp->skiped_frames = 0;

  svq1_error = 0;

  current_time = 0.0;
  current_frame_time = 1.0 / amp->framerate;
