#define MASK7F 0x7f7f7f7f

#define AVG2(a, b) \
  ((a) | (b)) - ((((a) ^ (b)) >> 1) & M7F)

#define AVG4(a, b, c, d) \
  AVG2((AVG2(a, b)), (AVG2(c, d)))

#define LONG(var, offset) *(unsigned long *)(var + offset)

void MC_put_8_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  4);

     LONG(dest,  0) = a;
     LONG(dest,  4) = b;

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_16_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  4);
     c = LONG(ref,  8);
     d = LONG(ref, 12);

     LONG(dest,  0) = a;
     LONG(dest,  4) = b;
     LONG(dest,  8) = c;
     LONG(dest, 12) = d;

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_x8_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d, M7F = MASK7F;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  4);

     c = LONG(ref,  1);
     d = LONG(ref,  5);

     LONG(dest,  0) = AVG2(a, c);
     LONG(dest,  4) = AVG2(b, d);

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_x16_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d, M7F = MASK7F;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  4);

     c = LONG(ref,  1);
     d = LONG(ref,  5);

     LONG(dest,  0) = AVG2(a, c);
     LONG(dest,  4) = AVG2(b, d);

     a = LONG(ref,  8);
     b = LONG(ref, 12);

     c = LONG(ref,  9);
     d = LONG(ref, 13);

     LONG(dest,  8) = AVG2(a, c);
     LONG(dest, 12) = AVG2(b, d);

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_y8_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d, M7F = MASK7F;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  4);
     c = LONG(ref + stride,  0);
     d = LONG(ref + stride,  4);

     LONG(dest,  0) = AVG2(a, c);
     LONG(dest,  4) = AVG2(b, d);

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_y16_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d, e, f, g, h, M7F = MASK7F;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  4);
     c = LONG(ref,  8);
     d = LONG(ref, 12);
     e = LONG(ref + stride,  0);
     f = LONG(ref + stride,  4);
     g = LONG(ref + stride,  8);
     h = LONG(ref + stride, 12);

     LONG(dest,  0) = AVG2(a, e);
     LONG(dest,  4) = AVG2(b, f);
     LONG(dest,  8) = AVG2(c, g);
     LONG(dest, 12) = AVG2(d, h);

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_xy8_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d, e, f, g, h, M7F = MASK7F;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  1);
     c = LONG(ref,  4);
     d = LONG(ref,  5);

     e = LONG(ref + stride,  0);
     f = LONG(ref + stride,  1);
     g = LONG(ref + stride,  4);
     h = LONG(ref + stride,  5);

     LONG(dest,  0) = AVG4(a, b, e, f);
     LONG(dest,  4) = AVG4(c, d, g, h);

     ref += stride;
     dest += stride;
    } while (--height);
}

void MC_put_xy16_c (uint8_t * dest, uint8_t * ref, int stride, int height)
{
    unsigned long a, b, c, d, e, f, g, h, M7F = MASK7F;

    do {
     a = LONG(ref,  0);
     b = LONG(ref,  1);
     c = LONG(ref,  4);
     d = LONG(ref,  5);

     e = LONG(ref + stride,  0);
     f = LONG(ref + stride,  1);
     g = LONG(ref + stride,  4);
     h = LONG(ref + stride,  5);

     LONG(dest,  0) = AVG4(a, b, e, f);
     LONG(dest,  4) = AVG4(c, d, g, h);

     a = LONG(ref,  8);
     b = LONG(ref,  9);
     c = LONG(ref, 12);
     d = LONG(ref, 13);

     e = LONG(ref + stride,  8);
     f = LONG(ref + stride,  9);
     g = LONG(ref + stride, 12);
     h = LONG(ref + stride, 13);

     LONG(dest,  8) = AVG4(a, b, e, f);
     LONG(dest, 12) = AVG4(c, d, g, h);

     ref += stride;
     dest += stride;
    } while (--height);
}

